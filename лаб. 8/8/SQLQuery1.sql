go
CREATE VIEW [Оборудование 2]
AS
SELECT Оборудование.[Название оборудования], СПИСАНИЯ.Название, СПИСАНИЯ.[Номер списания], COUNT(*) AS [Количество]
FROM Оборудование
JOIN СПИСАНИЯ ON Оборудование.[Название оборудования] = СПИСАНИЯ.Название
GROUP BY Оборудование.[Название оборудования], СПИСАНИЯ.Название, СПИСАНИЯ.[Номер списания]
go
select * from [Оборудование 2]

