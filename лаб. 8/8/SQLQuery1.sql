CREATE VIEW [Оборудование 6] with SCHEMABINDING
AS
SELECT o.[Название оборудования], s.Название, s.[Номер списания], 
COUNT(*) AS [Количество]
FROM dbo.Оборудование o JOIN dbo.СПИСАНИЯ s ON o.[Название оборудования] = s.Название
GROUP BY o.[Название оборудования], s.Название, s.[Номер списания]
go
select * from [Оборудование 6]

